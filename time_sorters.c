//a simple swap function
void swap(int* a, int* b) {
  int t = *a;
  *a = *b;
  *b = t;
}

//timer function
double measure_sort_time(SortFunction sort_func, int arr[], int size) {
    int* temp = malloc(size * sizeof(int));
    if (temp == NULL) {
        perror("Failed to allocate memory");
        exit(EXIT_FAILURE);
    }

    for (int i = 0; i < size; i++) {
        temp[i] = arr[i];
    }

    clock_t start_time = clock();

    sort_func(temp, size);

    clock_t end_time = clock();

    double time_taken = ((double)(end_time - start_time) / CLOCKS_PER_SEC);

    free(temp);
    return time_taken;
}


//bubbleSort
//source: self coded
void bubbleSort(int arr[], int n) {
  for (int i = 0; i < n - 1; i++) {

    int swapped = 0;

    for (int j = 0; j < n - i - 1; j++) {
      if (arr[j] > arr[j + 1]){
        swap(&arr[j], &arr[j + 1]);
        swapped = 1;
      }
    }

    if (swapped == 0) {
      break;
    }
  }
}

//quick sort
//source: https://www.geeksforgeeks.org/quick-sort-algorithm/ 
//              with a small modification
int partition(int arr[], int low, int high) {

  // Choose the pivot
  int pivot = arr[high];

  // Index of smaller element and indicates 
  // the right position of pivot found so far
  int i = low - 1;

  // Traverse arr[low..high] and move all smaller
  // elements to the left side. Elements from low to 
  // i are smaller after every iteration
  for (int j = low; j <= high - 1; j++) {
    if (arr[j] < pivot) {
      i++;
      swap(&arr[i], &arr[j]);
    }
  }

  // Move pivot after smaller elements and
  // return its position
  swap(&arr[i + 1], &arr[high]);  
  return i + 1;
}

// The QuickSort function implementation
void quickSort(int arr[], int low, int high) {
  if (low < high) {

    // pi is the partition return index of pivot
    int pi = partition(arr, low, high);

    // Recursion calls for smaller elements
    // and greater or equals elements
    quickSort(arr, low, pi - 1);
    quickSort(arr, pi + 1, high);
  }
}
//simple function caller 
void quickSortCaller(int arr[], int n) {
  quickSort(arr, 0, n - 1);
}


//selectionSort
//source: self coded
void selectionSort(int arr[], int n) {
  for (int i = 0; i < n - 1; i++) {
    int min_idx = i;
    for (int j = i + 1; j < n; j++) {
      if (arr[j] < arr[min_idx])
        min_idx = j;
    }
    swap(&arr[i], &arr[min_idx]);
  }
}


// insertionSort
// source: https://www.geeksforgeeks.org/c-program-for-insertion-sort/
void insertionSort(int arr[], int N) {

  // Starting from the second element
  for (int i = 1; i < N; i++) {
    int key = arr[i];
    int j = i - 1;

    // Move elements of arr[0..i-1], that are
    // greater than key, to one position to
    // the right of their current position
    while (j >= 0 && arr[j] > key) {
      arr[j + 1] = arr[j];
      j = j - 1;
    }

    // Move the key to its correct position
    arr[j + 1] = key;
  }
}

//radix sort
//source: https://www.geeksforgeeks.org/c-program-for-radix-sort/
int getMax(int arr[], int n) {
  int mx = arr[0];
  for (int i = 1; i < n; i++)
    if (arr[i] > mx)
      mx = arr[i];
  return mx;
}

// A function to do counting sort of arr[] according to
// the digit represented by exp.
void countSort(int arr[], int n, int exp) {
    // Find the maximum and minimum values in arr
    int max = INT_MIN, min = INT_MAX;
    for (int i = 0; i < n; i++) {
        if (arr[i] > max) max = arr[i];
        if (arr[i] < min) min = arr[i];
    }

    // Range of the counting array
    int range = max - min + 1;
    int *count = (int *)malloc(range * sizeof(int)); // Dynamic allocation for counting array
    if (count == NULL) {
        perror("Failed to allocate memory for count array");
        return;
    }

    // Output array
    int *output = (int *)malloc(n * sizeof(int)); // Dynamic memory allocation
    if (output == NULL) {
        perror("Failed to allocate memory for output array");
        free(count); // Free the count array if output allocation fails
        return;
    }

    // Initialize count array
    for (int i = 0; i < range; i++)
        count[i] = 0;

    // Store count of occurrences in count[]
    for (int i = 0; i < n; i++) {
        count[arr[i] - min]++; // Shift index to handle negative numbers
    }

    // Change count[i] so that it contains actual positions
    for (int i = 1; i < range; i++)
        count[i] += count[i - 1];

    // Build the output array
    for (int i = n - 1; i >= 0; i--) {
        output[count[arr[i] - min] - 1] = arr[i];
        count[arr[i] - min]--; // Decrement count
    }

    // Copy the output array to arr[]
    for (int i = 0; i < n; i++)
        arr[i] = output[i];

    // Free allocated memory
    free(count);
    free(output);
}

// The main function to that sorts arr[] of size n using
// Radix Sort
void radixsort(int arr[], int n) {
  // Find the maximum number to know number of digits
  int m = getMax(arr, n);

  // Do counting sort for every digit. Note that instead
  // of passing digit number, exp is passed. exp is 10^i
  // where i is current digit number
  for (int exp = 1; m / exp > 0; exp *= 10)
    countSort(arr, n, exp);
}

